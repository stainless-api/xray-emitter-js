test('encodeBase62Lex and decodeBase62Lex vectors', () => {
  for (const [input, base62b64Value] of testCases) {
    const bytes = Uint8Array.from(input);
    const expected = toLex(base62b64Value);
    const encoded = encodeBase62Lex(bytes);
    assert.equal(encoded, expected);

    const decoded = decodeBase62Lex(expected);
    assert.deepEqual(Array.from(decoded), input);
  }
});

test('encodeBase62Lex and decodeBase62Lex random round-trip', () => {
  const lengths = [1, 2, 3, 4, 5, 8, 16, 32, 100];
  for (const length of lengths) {
    let encodedLength = 0;
    for (let i = 0; i < 1000; i += 1) {
      const input = randomBytes(length);
      const encoded = encodeBase62Lex(input);
      if (i === 0) {
        encodedLength = encoded.length;
      } else {
        assert.equal(encoded.length, encodedLength);
      }

      const decoded = decodeBase62Lex(encoded);
      assert.deepEqual(Array.from(decoded), Array.from(input));
    }
  }
});
